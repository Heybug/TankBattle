function line () {
    game.ctx.strokeStyle = '#fff';
    for (var i = 0; i < 800; i += 32) {
        game.ctx.beginPath();
        game.ctx.lineWidth = 1;
        game.ctx.moveTo(i, HEIGHT);
        game.ctx.lineTo(i, 0);
        game.ctx.stroke()
    }
    for (var i = 0; i < 800; i += 32) {
        game.ctx.beginPath();
        game.ctx.lineWidth = 1;
        game.ctx.moveTo(WIDTH, i);
        game.ctx.lineTo(0, i);
        game.ctx.stroke()
    }
}

